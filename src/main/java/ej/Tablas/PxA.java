package ej.Tablas;

public class PxA {
	private int id;
	private Area area;
	private Pregunta pregunta;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public Area getArea() {
		return area;
	}
	
	public void setArea(Area area) {
		this.area = area;
	}
	
	public Pregunta getPregunta() {
		return pregunta;
	}
	
	public void setPregunta(Pregunta pregunta) {
		this.pregunta = pregunta;
	}
}
